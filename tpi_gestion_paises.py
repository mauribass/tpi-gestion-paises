# variables globales
ARCHIVO_CSV = "paises.csv"
CAMPOS = ["nombre", "poblacion", "superficie", "continente"]
CONTINENTES = ["Asia", "América", "Europa", "África", "Oceanía"]

"""Lee el archivo CSV y retorna una lista de diccionarios.
Si el archivo no existe retorna una lista vacía. """
def cargar_paises():
    # Lista vacía que se irá llenando con los datos del CSV
    paises = []
    try:
        # Abre el archivo CSV en modo lectura con codificación utf-8
        # (utf-8 es necesario para que lea correctamente tildes y caracteres especiales)
        # newline='' evita que Python agregue saltos de línea extra al leer
        with open(ARCHIVO_CSV, newline='', encoding='utf-8') as f:

            # DictReader lee el CSV y convierte cada fila en un diccionario,
            # usando la primera fila (encabezado) como claves.
            # Ejemplo: {"nombre": "Argentina", "poblacion": "45376763", ...}
            reader = csv.DictReader(f)

            for fila in reader:
                # Por defecto DictReader lee todos los valores como strings.
                # Convertimos poblacion y superficie a int para poder operar
                # numéricamente (comparar, ordenar, calcular promedios, etc).
                # strip() elimina espacios en blanco al inicio y al final del string.
                paises.append({
                    "nombre": fila["nombre"].strip(),
                    "poblacion": int(fila["poblacion"]),
                    "superficie": int(fila["superficie"]),
                    "continente": fila["continente"].strip()
                })

    except FileNotFoundError:
        # Error específico cuando el archivo CSV no existe en la ruta indicada
        print("El archivo no existe.")
    except Exception as e:
        # Captura cualquier otro error inesperado (formato incorrecto, permisos, etc.)
        print(f"No se pudo leer el archivo: {e}")

    # Retorna la lista con todos los países cargados.
    # Si hubo un error, retorna la lista vacía.
    return paises

# Esta funcion sirve para actualizar el archivo csv cada vez que el usuario aplique un cambio.
def guardar_paises(paises):
  try:
      with open(ARCHIVO_CSV, "w", newline='', encoding='utf-8') as f:
          writer = csv.DictWriter(f, fieldnames=CAMPOS)
          writer.writeheader()
          writer.writerows(paises)
  except Exception as e:
      print(f"[ERROR] No se pudo guardar el archivo: {e}")

# funcion auxiliar para verificar si existe un pais en el archivo
def existe_pais(paises, nombre):
    for pais in paises:
        if pais["nombre"].strip().lower() == nombre.strip().lower():
            return True
    return False

# funcion para que el usuario agregue paises
def agregar_paises(paises):
  print("---- Nuevo país ----")
  try:  # entradas solicitadas al usuario
        nombre = input("Nombre del nuevo país: ").strip()
        if nombre == "":
            raise ValueError("El nombre del pais no puede ser vacío.")
        if existe_pais(paises, nombre):
            raise ValueError(f"El pais '{nombre}' ya esta cargado en el sistema.")

        poblacion = int(input("Numero de población: "))
        if poblacion <= 0:
            raise ValueError("El numero de poblacion no puede ser menor a 0.")

        superficie = int(input("Superificie km²: "))
        if superficie <=0:
            raise ValueError("La superficie no puede ser menor a 0.")

        continente = input(f"Continente al que pertenece {nombre}: ").strip()
        # Valida que el continente ingresado exista en CONTINENTES.
        # Compara en minúsculas para ignorar mayúsculas/minúsculas.
        while continente.lower() not in [c.lower() for c in CONTINENTES]:
            print(f"Continente inválido. Los válidos son: {', '.join(CONTINENTES)}")
            continente = input("Ingresá nuevamente el continente: ").strip()

        # Recupera el valor oficial de la lista CONTINENTES.
        # Garantiza que se guarde con la escritura correcta (tildes y mayúsculas).
        # Ejemplo: "america" → "América", "europa" → "Europa"
        continente = next(c for c in CONTINENTES if c.lower() == continente.lower())

        # Agrega el nuevo país a la lista como un diccionario con sus cuatro campos
        paises.append({"nombre": nombre, "poblacion": poblacion, "superficie": superficie, "continente": continente})

        # Persiste los cambios escribiendo la lista actualizada en el archivo CSV
        guardar_paises(paises)
        print(f"'{nombre}' agregado correctamente.")

  except ValueError as e:
        print(f"Error: {e} Volviendo al menú.")

# funcion auxiliar para actualizar_datos
def aux_buscar_exacto(paises, nombre):
    for pais in paises:
        if pais["nombre"].strip().lower() == nombre.strip().lower():
            return pais
    return None

# Permite al usuario modificar nombre, población, superficie o continente de un país existente
def actualizar_datos(paises):
    print("--- Actualización de Datos ----")

    # Repite la solicitud hasta que el usuario ingrese un nombre no vacío
    busqueda_valida = False
    while not busqueda_valida:
        try:
            busqueda = input("Qué país desea actualizar?: ")
            if busqueda == "":
                raise ValueError("La búsqueda no puede estar vacía.")
            busqueda_valida = True
        except ValueError as e:
            print(f"Error: {e} Intente nuevamente.")

    # Busca el país por coincidencia exacta de nombre
    pais = aux_buscar_exacto(paises, busqueda)

    if pais:
        # Muestra los datos actuales del país antes de modificar
        print(f"País: {pais['nombre']} | Población: {pais['poblacion']} personas | Superficie: {pais['superficie']}km² | Continente: {pais['continente']}")

        # Submenú con los campos disponibles para modificar
        print("\n1. Nombre")
        print("2. Población")
        print("3. Superficie")
        print("4. Continente")
        try:
            dato = int(input("Seleccione el dato a modificar: "))
            if dato not in (1, 2, 3, 4):
                raise ValueError("Opción inválida.")

            if dato == 1:
                nuevo = input("Nuevo nombre: ").strip()
                if nuevo == "":
                    raise ValueError("El nombre no puede estar vacío.")
                # Verifica que no exista otro país con el mismo nombre
                if existe_pais(paises, nuevo):
                    raise ValueError(f"'{nuevo}' ya existe en el sistema.")
                pais["nombre"] = nuevo

            elif dato == 2:
                nuevo = int(input("Nueva población: "))
                if nuevo <= 0:
                    raise ValueError("La población debe ser mayor a 0.")
                pais["poblacion"] = nuevo

            elif dato == 3:
                nuevo = int(input("Nueva superficie km²: "))
                if nuevo <= 0:
                    raise ValueError("La superficie debe ser mayor a 0.")
                pais["superficie"] = nuevo

            elif dato == 4:
                nuevo = input("Nuevo continente: ").strip()
                # Valida que el continente ingresado exista en la lista CONTINENTES
                while nuevo.lower() not in [c.lower() for c in CONTINENTES]:
                    print(f"Continente inválido. Los válidos son: {', '.join(CONTINENTES)}")
                    nuevo = input("Ingresá nuevamente el continente: ").strip()
                # Recupera el valor oficial con tildes y mayúsculas correctas
                nuevo = next(c for c in CONTINENTES if c.lower() == nuevo.lower())
                pais["continente"] = nuevo

            # Como pais es una referencia al diccionario dentro de la lista,
            # cualquier cambio hecho arriba ya modificó la lista paises directamente.
            # Solo resta persistir los cambios en el archivo CSV.
            guardar_paises(paises)
            print(f"Dato actualizado correctamente.")

        except ValueError as e:
            print(f"Error: {e} Volviendo al menú.")
    else:
        print(f"'{busqueda}' no está cargado en el sistema.")

