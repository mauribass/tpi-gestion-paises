# filtrar por continente
def filtrar_por_continente(paises):
    # Solicita al usuario el continente a filtrar
    continente = input("Ingresá el continente a filtrar: ").strip()

    # Valida que el continente ingresado exista en la lista CONTINENTES.
    # La comparación se hace en minúsculas de ambos lados para ignorar mayúsculas.
    while continente.lower() not in [c.lower() for c in CONTINENTES]:
        print(f"Continente inválido. Los válidos son: {', '.join(CONTINENTES)}")
        continente = input("Ingresá nuevamente el continente: ").strip()

    # Una vez validado, recupera el valor "oficial" de la lista (con tildes y mayúsculas correctas).
    # Ejemplo: si el usuario escribió "america", lo reemplaza por "América".
    continente = next(c for c in CONTINENTES if c.lower() == continente.lower())

    # Filtra la lista de paises quedándose solo con los que pertenecen al continente elegido.
    # Usa una lista por comprensión para recorrer y evaluar cada país en una sola línea.
    resultados = [p for p in paises if p["continente"] == continente]

    if resultados:
        # Ordena los resultados alfabéticamente por nombre
        # El parámetro key indica el criterio de ordenamiento (nombre en minúsculas).
        resultados.sort(key=lambda p: p["nombre"].lower())

        print(f"\n{len(resultados)} país/es encontrado/s en {continente}:")
        for pais in resultados:
            print(f"País: {pais['nombre']} | Población: {pais['poblacion']} personas | Superficie: {pais['superficie']}km² | Continente: {pais['continente']}")
    else:
        print(f"No se encontraron países en '{continente}'.")

# filtrar por rango de poblacion
def filtrar_por_poblacion(paises):
    print("Ingresá el rango de población (dejá vacío para omitir un extremo):")
    try:
        # Guarda el input como string para poder verificar si está vacío antes de convertir a int
        minimo_input = input("Población mínima: ").strip()
        maximo_input = input("Población máxima: ").strip()

        # Si el usuario dejó el campo vacío, el extremo queda como None (sin límite).
        # Si ingresó un valor, se convierte a int para poder comparar numéricamente.
        minimo = int(minimo_input) if minimo_input != "" else None
        maximo = int(maximo_input) if maximo_input != "" else None

        # Validaciones: solo se ejecutan si el extremo fue ingresado (no es None)
        if minimo is not None and minimo < 0:
            raise ValueError("El mínimo no puede ser negativo.")
        if maximo is not None and maximo < 0:
            raise ValueError("El máximo no puede ser negativo.")
        # Solo tiene sentido comparar mínimo y máximo si ambos fueron ingresados
        if minimo is not None and maximo is not None and minimo > maximo:
            raise ValueError("El mínimo no puede ser mayor al máximo.")

        resultados = []
        for p in paises:
            # Si se definió un mínimo y el país no lo alcanza, se saltea con continue
            if minimo is not None and p["poblacion"] < minimo:
                continue
            # Si se definió un máximo y el país lo supera, se saltea con continue
            if maximo is not None and p["poblacion"] > maximo:
                continue
            # Si pasó ambas condiciones, se agrega a los resultados
            resultados.append(p)

        if resultados:
            # Ordena los resultados de mayor a menor población
            resultados.sort(key=lambda p: p["poblacion"], reverse=True)
            print(f"\n{len(resultados)} país/es encontrado/s:")
            for pais in resultados:
                print(f"País: {pais['nombre']} | Población: {pais['poblacion']} personas | Superficie: {pais['superficie']}km² | Continente: {pais['continente']}")
        else:
            print("No se encontraron países en ese rango de población.")

    except ValueError as e:
        print(f"Error: {e} Volviendo al menú.")

