# ---- modulo de ordenamiento ----
# el usuario puede ordenar los paises segun distintos criterios (nombre, poblacion y superficie)

def ordenar_paises(paises, criterio, ascendente):
    # Ordena la lista según el criterio elegido y la dirección indicada.
    # reverse=False → ascendente, reverse=True → descendente
    if criterio == "nombre":
        # Para nombre se convierte a minúsculas para ignorar mayúsculas al ordenar
        return sorted(paises, key=lambda p: p["nombre"].lower(), reverse=not ascendente)
    elif criterio == "poblacion":
        return sorted(paises, key=lambda p: p["poblacion"], reverse=not ascendente)
    elif criterio == "superficie":
        return sorted(paises, key=lambda p: p["superficie"], reverse=not ascendente)

def menu_ordenar(paises):
    print("\n---- Ordenar Países ----")
    print("1. Por nombre")
    print("2. Por población")
    print("3. Por superficie")
    print("0. Volver al menú principal")

    opcion = input("Seleccione un criterio: ").strip()

    if opcion == "0":
        return
    if opcion not in ("1", "2", "3"):
        print("Opción inválida.")
        return

    # Mapea la opción elegida al nombre del campo correspondiente
    criterios = {"1": "nombre", "2": "poblacion", "3": "superficie"}
    criterio = criterios[opcion]

    # Submenú para elegir la dirección del ordenamiento
    print("\n1. Ascendente")
    print("2. Descendente")
    direccion = input("Seleccione el orden: ").strip()

    if direccion not in ("1", "2"):
        print("Opción inválida.")
        return

    # True si eligió ascendente, False si eligió descendente
    ascendente = direccion == "1"

    resultados = ordenar_paises(paises, criterio, ascendente)

    direccion_texto = "ascendente" if ascendente else "descendente"
    print(f"\nPaíses ordenados por {criterio} ({direccion_texto}):")
    for pais in resultados:
        print(f"País: {pais['nombre']} | Población: {pais['poblacion']} personas | Superficie: {pais['superficie']}km² | Continente: {pais['continente']}")

# ---- modulo estadisticas ----
""" el ususario cuenta con las siguientes opciones:
- pais con menor y mayor poblacion.
- promedio de poblacion.
- promedio de superficie
- pais por continente"""

def mostrar_estadisticas(paises):
    print("\n---- Estadísticas ----")

    # Verifica que haya países cargados antes de operar
    if not paises:
        print("No hay países cargados en el sistema.")
        return

    # --- País con mayor y menor población ---
    # max() y min() recorren la lista y devuelven el diccionario
    # cuyo campo "poblacion" es el mayor/menor respectivamente
    pais_mayor_pob = max(paises, key=lambda p: p["poblacion"])
    pais_menor_pob = min(paises, key=lambda p: p["poblacion"])

    print(f"\nPaís con mayor población: {pais_mayor_pob['nombre']} ({pais_mayor_pob['poblacion']} personas)")
    print(f"País con menor población: {pais_menor_pob['nombre']} ({pais_menor_pob['poblacion']} personas)")

    # --- Promedios ---
    # sum() acumula el valor del campo indicado de todos los países,
    # se divide por la cantidad total para obtener el promedio.
    # int() elimina los decimales del resultado.
    promedio_poblacion = int(sum(p["poblacion"] for p in paises) / len(paises))
    promedio_superficie = int(sum(p["superficie"] for p in paises) / len(paises))

    print(f"\nPromedio de población: {promedio_poblacion} personas")
    print(f"Promedio de superficie: {promedio_superficie} km²")

    # --- Cantidad de países por continente ---
    # Recorre la lista y construye un diccionario donde cada clave es un continente
    # y su valor es la cantidad de países que pertenecen a ese continente.
    conteo_continentes = {}
    for p in paises:
        continente = p["continente"]
        if continente in conteo_continentes:
            # Si el continente ya existe en el diccionario, incrementa el contador
            conteo_continentes[continente] += 1
        else:
            # Si el continente no existe todavía, lo inicializa en 1
            conteo_continentes[continente] = 1

    print("\nCantidad de países por continente:")
    for continente, cantidad in conteo_continentes.items():
        print(f"  {continente}: {cantidad} país/es")

